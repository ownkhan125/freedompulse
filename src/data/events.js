export function getEvent(slug) {
  return EVENTS.find((e) => e.id === slug);
}

export function getRelatedEvents(slug, count = 3) {
  const event = getEvent(slug);
  if (!event) return [];
  const sameCategory = EVENTS.filter(
    (e) => e.id !== slug && e.category === event.category,
  );
  const others = EVENTS.filter((e) => e.id !== slug && e.category !== event.category);
  return [...sameCategory, ...others].slice(0, count);
}
